export function TranslationProvider({ children }) {
  const [lang, setLang] = useState(() => {
    return localStorage.getItem('nimbus_lang') || 'en';
  });

  function changeLanguage(newLang) {
    if (translations[newLang]) {
      setLang(newLang);
      localStorage.setItem('nimbus_lang', newLang);
    }
  }

  function t(key, defaultVal, replacements) {
    if (typeof defaultVal === 'object' && defaultVal !== null) {
      replacements = defaultVal;
      defaultVal = undefined;
    }
    let translation = translations[lang]?.[key] || translations['en']?.[key];
    if (translation === undefined) {
      translation = defaultVal !== undefined ? defaultVal : key;
    }
    if (replacements && typeof translation === 'string') {
      Object.entries(replacements).forEach(([k, v]) => {
        translation = translation.replace(new RegExp(`{{${k}}}`, 'g'), String(v));
      });
    }
    return translation;
  }

  const languages = [
    { code: 'en', label: 'English' },
    { code: 'es', label: 'Español' },
    { code: 'fr', label: 'Français' },
  ];

  return (
    <i18nContext.Provider value={{ t, currentLanguage: lang, changeLanguage, languages }}>
      {children}
    </i18nContext.Provider>
  );
}

export function useTranslation() {
  const context = useContext(i18nContext);
  if (!context) {
    throw new Error('useTranslation must be used within a TranslationProvider');
  }
  return context;
}

export function LanguageSelector({ style }) {
  const { currentLanguage, changeLanguage, languages } = useTranslation();
  return (
    <div style={{ display: 'flex', alignItems: 'center', gap: 6, ...style }}>
      <Globe size={14} style={{ color: 'var(--text-secondary)' }} />
      <select
        value={currentLanguage}
        onChange={(e) => changeLanguage(e.target.value)}
        style={{
          background: 'var(--color-surface-2)',
          border: '1px solid var(--color-border-strong)',
          borderRadius: 'var(--radius-sm)',
          padding: '6px 10px',
          color: 'var(--text-secondary)',
          fontFamily: 'var(--font-sans)',
          fontSize: '13px',
          outline: 'none',
          cursor: 'pointer',
        }}
      >
        {languages.map((l) => (
          <option
            key={l.code}
            value={l.code}
            style={{
              background: 'var(--nimbus-charcoal-900)',
              color: 'var(--text-primary)',
            }}
          >
            {l.label}
          </option>
        ))}
      </select>
    </div>
  );
}
